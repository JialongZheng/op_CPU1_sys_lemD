Uint16 get_posedge(Uint16 Value )
{
    static Uint16 Previous;

    if (Value && !Previous)
    {
        Previous = 1;
        return 1;
    }
    if (!Value)
        Previous = 0;
    return 0;
}
Uint16 get_16posedge(Uint16 Value ,Uint16 ch)
{
    static Uint16 prev_bit[5][16];//记录上一次的状态
    static Uint16 res;
    Uint16 i = 0;

    for ( i = 0; i < 16; i++)
    {  // 逐位扫描a的每个bit
        Uint16 curr_bit = (Uint16)(Value >> i) & 1;  // 获取a的第i位的值，0或1
        if (curr_bit == 1 && prev_bit[ch][i] == 0)
        {  // 出现上升沿
            res |= (Uint16)(1 << i);  // 将b的第i位设置为1
            prev_bit[ch][i] = 1;   // 更新上一个扫描的位的值为1
        }
        else if (curr_bit == 0) {
            prev_bit[ch][i] = 0;   // 更新上一个扫描的位的值为0
        }
    }
    return res;
}
Uint16 get_negedge(Uint16 Value)
{
    static Uint16 Previous;
    if (!Value && Previous)
    {
        Previous = 0;
        return 1;
    }
    if (Value)
        Previous = 1;
    return 0;
}
int16 CrcCalcWord(int16 *p,int16 len)
{
	int16 result= 0xffff;
	int16 index = 0;

	while(len > 0)
	{
		index  = (result ^ ((*p >> 8) & 0x00ff)) & 0x00ff;
		result = ((result>>8)&0x00ff)^CrcTable[index];
		index  = (result ^ (*p & 0x00ff)) & 0x00ff;
		result = ((result>>8)&0x00ff)^CrcTable[index];
		p++;
		len--;
	}
	return result;
}

int16 CrcCalc(char *p,int16 len)
{
	int16 result= 0xffff;
	int16 index = 0;
	while(len>0)
	{
		index = (result ^ *p) & 0x00ff;
		result = ((result>>8)&0x00ff)^CrcTable[index];
		p++;
		len--;
	}
	return result;
}



int16 CrcCalc8_8(char *p, int16 len)
{
	unsigned char CrcHi,CrcLo;
	unsigned char uIndex;
	unsigned int ret_val;

	CrcHi = 0xff;
	CrcLo = 0xff;
	while(len>0)
	{
		uIndex = (CrcLo ^ (*p)) & 0x00ff;
		p++;
		CrcLo = CrcHi ^ CrcTableHi[uIndex];
		CrcHi = CrcTableLo[uIndex];
		len--;
	}
	ret_val = CrcHi;
	ret_val = (ret_val<<8) + (CrcLo&0x00ff);
	return ret_val;
}


